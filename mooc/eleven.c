#include <stdio.h>
int main()
{
	int pro,bon;
	scanf("%d",&pro);
	if (pro<=100000)
		bon=0.1*pro;
	else if (pro<=200000)
		bon=0.1*100000+0.075*(pro-100000);
	else if (pro<=400000)
		bon=0.1*100000+0.075*100000+0.05*(pro-200000);
	else if (pro<=600000)
		bon=0.1*100000+0.075*100000+0.05*200000+0.03*(pro-400000);
	else if (pro<=1000000)
		bon=0.1*100000+0.075*100000+0.05*200000+0.03*200000+0.015*(pro-600000);
	else
		bon=0.1*100000+0.075*100000+0.05*200000+0.03*200000+0.015*400000+0.01*(pro-1000000);
	printf("%d",bon);

	return 0;
}